export class Cachorro {
    nome;
    idade;
    raca;
    porte;
    cor;

    constructor(nome, idade, raca, porte, cor) {
        this.nome = nome;
        this.idade = idade;
        this.raca = raca;
        this.porte = porte;
        this.cor = cor;
    }

    comer(valor){
        if (this.valor = 7){
            return true;
        }else
            return false;
    }

    dormir(valor){
        if (this.valor >= 7);
            return;
    }

    brincar(valor){
        if (this.valor > 12);
            return;
    }
}